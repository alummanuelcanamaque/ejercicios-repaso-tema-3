    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        area.setText("");
        Random random = new Random();
        
        int numDiaSemana=0;
        int numDiaAnterior=0;
        do{
            
            numDiaAnterior = numDiaSemana;
            numDiaSemana = random.nextInt(7);
            LOG.info("numDiaSemana: "+numDiaSemana);
            switch(numDiaSemana){
                case 0:
                    area.append("Lunes\n");
                    break;
                case 1:
                    area.append("Martes\n");
                    break;
                case 2:
                    area.append("Miercoles\n");
                    break;
                case 3:
                    area.append("Jueves\n");
                    break;
                case 4:
                    area.append("Viernes\n");
                    break;
                case 5:
                    area.append("Sabado\n");
                    break;
                case 6:
                    area.append("Domingo\n");
                    break; 
                default:
                    area.append("Error\n");
                    break;
        }    
        }while(numDiaAnterior<=numDiaSemana);
                   
        
    }//GEN-LAST:event_jButton1ActionPerformed

